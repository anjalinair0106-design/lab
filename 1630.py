#Q29 Aim: To apply and analyze Watershed Thresholding for image segmentation. • Convert the input image to grayscale. • Apply gradient-based edge detection. • Use Watershed algorithm to segment the image based on gradients. • Visualize and label the segmented regions.
import cv2, numpy as np, matplotlib.pyplot as plt

img = cv2.imread('/content/dwayne-joe-rLLP1G4ZRT0-unsplash.jpg')
gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)

# Threshold + noise removal
_,th = cv2.threshold(gray,0,255,cv2.THRESH_BINARY+cv2.THRESH_OTSU)
k = np.ones((3,3),np.uint8)
opening = cv2.morphologyEx(th, cv2.MORPH_OPEN, k)

# Sure background & foreground
bg = cv2.dilate(opening,k,3)
dist = cv2.distanceTransform(opening,cv2.DIST_L2,5)
_,fg = cv2.threshold(dist,0.7*dist.max(),255,0)

# Markers + Watershed
fg = np.uint8(fg)
unknown = cv2.subtract(bg,fg)
_,markers = cv2.connectedComponents(fg)
markers = markers + 1
markers[unknown==255] = 0

markers = cv2.watershed(img,markers)
img[markers==-1] = [255,0,0]   # boundaries

plt.imshow(cv2.cvtColor(img, cv2.COLOR_BGR2RGB))
plt.title('Watershed Segmentation')
plt.axis('off')

