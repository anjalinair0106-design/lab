#Q27 Aim: To compare spatial, frequency, and morphological processing. • Apply each method • Analyze differences
import cv2, numpy as np, matplotlib.pyplot as plt

img = cv2.imread('/content/dwayne-joe-rLLP1G4ZRT0-unsplash.jpg',0)

# Spatial (Gaussian)
sp = cv2.GaussianBlur(img,(5,5),0)

# Frequency (Low Pass)
f = np.fft.fftshift(np.fft.fft2(img))
r,c = img.shape; m = np.zeros((r,c),np.uint8)
m[r//2-30:r//2+30, c//2-30:c//2+30] = 1
fr = np.abs(np.fft.ifft2(np.fft.ifftshift(f*m)))

# Morphology (Opening)
k = np.ones((3,3),np.uint8)
mo = cv2.morphologyEx(img, cv2.MORPH_OPEN, k)

plt.subplot(141),plt.imshow(img,cmap='gray'),plt.title('Original')
plt.subplot(142),plt.imshow(sp,cmap='gray'),plt.title('Spatial')
plt.subplot(143),plt.imshow(fr,cmap='gray'),plt.title('Frequency')
